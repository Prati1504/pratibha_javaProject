package ifElse;

public class IfElse
{
	
	public static void main(String[] args) 
	{
	int x=10;
	int y=20;
	
	if(x==y)
		System.out.println("true");
	else
		System.out.println("false");
		
		
	if(x>=y)
		System.out.println(true);
	else
		System.out.println("false");
	
	if(x!=y)
		System.out.println("true");
	else
		System.out.println("false");
	
	if((x>y)||(x==y))
		System.out.println("true");
	else
		System.out.println("false");
	
	if((x>y)&&(x==y))
		System.out.println("true");
	else
		System.out.println("false");
	
	
	
		
	}
	
	
		
}
