package returnTypesAndArgumentMethods;

public class WithReturnWithoutArgument
{
	public int WithoutArgument()
	{
		return 30;
		
	}
	

	public static void main(String[] args)
	{
		WithReturnWithoutArgument n=new WithReturnWithoutArgument();	
		System.out.println(n.WithoutArgument());
	}

}
